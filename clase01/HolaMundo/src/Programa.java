// Un conjunto de lineas envueltas en llaves se llama BLOQUE

public class Programa {

    // El punto de entrada indica por dónde empieza a la ejecución
    // En java el punto de entrada es el método main:
    // En IntelliJ se escribe: main<Tab>

    public static void main(String[] args) {
        // Acá ya se pueden empezar a escribir instrucciones

        // Para poder imprimir: System.out.println();
        // Abreviatura: sout<Tab>

        System.out.println("Hola Mundo!!!!");
        System.out.println("Estoy en AP 4.0, en la clase 1");

        // Sumar dos números:
        // Voy a necesitar dos variables

        // Primero hay que declarar las variables
        // tipo identificador;
        int numero1 = 34;
        int numero2 = 67;

        int suma = numero1 + numero2;

        System.out.println("La suma es " + suma);
        // Ampliarlo para mostrar: diferencia, producto y cociente.

    }


}
